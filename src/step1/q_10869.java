package step1;

import java.util.Scanner;

public class q_10869 {

    /**
     * 10869  주어진 두수를 사칙연산 하라
     * @param args
     */
    public static void main(String[] args) {

        Scanner s = new Scanner(System.in);

        int a = s.nextInt();
        int b = s.nextInt();

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);




    }

}
